    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
if(txtNom.getText().length()==0|| 
                this.txtPrec.getText().length()==0||
                this.tblPro.getRowCount()==0||
                this.taDescr.getText().length()==0){
                JOptionPane.showMessageDialog(this, "Debe Ingresar Todos Los Datos", "Datos Incompletos", JOptionPane.ERROR_MESSAGE);
               }else{        // TODO add your handling code here:
     String vali = Funciones.PruductoValidate(txtNom.getText(), comboTam.getSelectedItem().toString(), compiez.getSelectedItem().toString());
     
        if (vali.equals("1")) {
            JOptionPane.showMessageDialog(null, "Esta pizza ya fue registrada");
        }else{
        Sentencias_Angel s = new Sentencias_Angel();
        s.setId_P(Integer.parseInt(this.lbId.getText()));
        s.setNombre_P(this.txtNom.getText());
        s.setPrecio_P(Double.parseDouble(this.txtPrec.getText()));
        s.setDescripcion_P(this.taDescr.getText());
        s.setTamanio_P(this.comboTam.getSelectedItem().toString());
        s.setNum_Piezas_P(this.compiez.getSelectedItem().toString()); 
        s.setExistencia_P(Integer.parseInt(this.txtExis.getText()));
               
        if(Funciones_Angel.isUpdate_P(s)){
            JOptionPane.showMessageDialog(this, "Nuevo Producto Creado");
            Funciones_Angel.setListar_P("");
            
            GuadarDetalle();
            dispose();
        }else{
            JOptionPane.showMessageDialog(this, "No se pudo crear el Producto");
        }
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

