    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
       try {
            Reportes reporte = new Reportes();
             HashMap datos = new HashMap();
             int fila = tbl.getSelectedRow();
             datos.put("id_dpl", Integer.parseInt(tbl.getValueAt(fila, 0).toString()));
             datos.put("observaciones", (tbl.getValueAt(fila, 1).toString()));
             datos.put("tipo", (tbl.getValueAt(fila, 2).toString()));
             datos.put("periodo", (tbl.getValueAt(fila, 3).toString()));
             datos.put("fecha_c", (tbl.getValueAt(fila, 4).toString()));
             datos.put("fecha_p", (tbl.getValueAt(fila, 5).toString()));
             
            reporte.ReportePlanilla(datos);
        } catch (JRException  ex) {
            Logger.getLogger(Reportes.class.getName()).log(Level.SEVERE, null, ex);
        } catch(SQLException ex){
            Logger.getLogger(Reportes.class.getName()).log(Level.SEVERE, null, ex);
        }
       // TODO add your handling code here:
    }//GEN-LAST:event_btnImprimirActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int fila = tbl.getSelectedRow();
        
        Sentencias s = new Sentencias();
        s.setId_Detalle_Pla(Integer.parseInt(tbl.getValueAt(fila, 0).toString()));
        
        
        s.setId_Pla(Integer.parseInt(tbl.getValueAt(fila, 0).toString()));
        
        int a= JOptionPane.showConfirmDialog(null, "esta seguro", "mostrar", JOptionPane.YES_NO_OPTION);
        if (a==JOptionPane.YES_NO_OPTION) {
            
        
        if(Funciones.isDeleteDEP(s)){
            Funciones.isDeletePlanilla(s);
            
            JOptionPane.showMessageDialog(this, "PLANILLA BORRADA EXITOSAMENTE");
            Funciones.setListarJoinPLANILLA();
        }else{
            JOptionPane.showMessageDialog(this, "Nada");
            
            
        }
        }else{
            JOptionPane.showMessageDialog(null, "No se realizo el borrado");
        }// TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

