    private void txnombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txnombreKeyTyped
        // TODO add your handling code here:
        txnombre.setText(txnombre.getText().replaceAll("( )+", " "));
        if (txnombre.getText().length()==0 && evt.getKeyChar()==KeyEvent.VK_SPACE) {
            evt.consume();          
        }
        
        
        char c = evt.getKeyChar();
         if(Character.isDigit(c)){
            getToolkit().beep();
            evt.consume();
         }
            int tam = txnombre.getText().length();
        if(tam>=30){
            evt.consume();
        }
    }//GEN-LAST:event_txnombreKeyTyped

