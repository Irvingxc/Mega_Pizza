    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
         if(txnombre.getText().length()==0 || 
                this.txtmarca.getText().length()==0||
                this.combouni.getSelectedItem().equals(0)||                
                this.existencia.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Todo los campos son requeridos");
        }else{
            Sentencias_Angel s = new Sentencias_Angel();
            s.setId_IMP(Integer.parseInt(this.la_Id.getText()));
            s.setNombre_IMP(this.txnombre.getText());
            s.setMarca_IMP(this.txtmarca.getText());
            s.setUnidad_Medida_IMP(this.combouni.getSelectedItem().toString());
            s.setExistencia_IMP(Integer.parseInt(this.existencia.getText()));
            
            
            
            if(Funciones_Angel.isUpdateInve(s)){
               // Funciones.setListar("");
            JOptionPane.showMessageDialog(this, "ActualizaciÃ³n correcta");
            Funciones_Angel.setListarInve("");
            
            }else{
                JOptionPane.showMessageDialog(this, "Fallo de Actualizacion");
                
            }
            dispose();
            
            
        }
    }//GEN-LAST:event_btnguardarActionPerformed

