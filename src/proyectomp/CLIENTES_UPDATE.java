    private void txtcupnomKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcupnomKeyTyped
JTextField txtnombre = (JTextField)evt.getComponent();

String texto=txtnombre.getText();//.trim();//para evitar espacios en blanco antes y despues del texto
if(texto.length()>0){
char primero=texto.charAt(0);
texto=Character.toUpperCase(primero)+texto.substring(1, texto.length());
txtnombre.setText(texto);
}        // TODO add your handling code here:
        txtcupnom.setText(txtcupnom.getText().replaceAll("( )+", " "));
        if (txtcupnom.getText().length()==0 && evt.getKeyChar()==KeyEvent.VK_SPACE) {
            evt.consume();          
        }
        int tam = txtcupnom.getText().length();
        if(tam>=30){
            evt.consume();
        }
    }//GEN-LAST:event_txtcupnomKeyTyped

