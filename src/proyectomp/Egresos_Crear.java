    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        if(txtNombre.getText().length()==0 || 
                this.txtMonto.getText().length()==0||
                this.calendarFechha.getDate().equals(null)||
                this.txArDescrip.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Todo los campos son requeridos");
        }else{
            Sentencias_Angel s = new Sentencias_Angel();
            s.setNombre_Egre(this.txtNombre.getText());
            s.setMonto_Egre(Double.parseDouble(this.txtMonto.getText()));
            s.setFecha_Egre(((JTextField)this.calendarFechha.getDateEditor().getUiComponent()).getText());
            s.setDescripcion_Egre(this.txArDescrip.getText());
            
            
            if(Funciones_Angel.isRegisterEgre(s)){
               // Funciones.setListar("");
            JOptionPane.showMessageDialog(this, "Nuevo Ingreso Creado");
            Funciones_Angel.setListarEgr("");
            
            }else{
                JOptionPane.showMessageDialog(this, "Fallo el Registro");
                
            }
            dispose();      
        }
    }//GEN-LAST:event_guardarActionPerformed

