    private void xtxBuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_xtxBuscarKeyTyped
        DefaultTableModel m = (DefaultTableModel) tblInventario.getModel();
        xtxBuscar.addKeyListener(new KeyAdapter() { 

            @Override
            public void keyReleased(KeyEvent ke) {
                trs.setRowFilter(RowFilter.regexFilter("(?i)"+xtxBuscar.getText(), 1));
                
                super.keyReleased(ke); //To change body of generated methods, choose Tools | Templates.
            }
        
        
        });
        trs = new TableRowSorter(m);
        tblInventario.setRowSorter(trs);
        
    }//GEN-LAST:event_xtxBuscarKeyTyped

